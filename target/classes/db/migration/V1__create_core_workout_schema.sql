CREATE TABLE app_users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    email VARCHAR(180) NOT NULL,
    password VARCHAR(255) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT uk_app_users_email UNIQUE (email)
);

CREATE TABLE gyms (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    name VARCHAR(120) NOT NULL,
    address VARCHAR(255),
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE equipment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    gym_id BIGINT,
    name VARCHAR(120) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_equipment_gym FOREIGN KEY (gym_id) REFERENCES gyms (id)
);

CREATE TABLE exercises (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    equipment_id BIGINT,
    name VARCHAR(120) NOT NULL,
    muscle_group VARCHAR(120),
    instructions VARCHAR(1000),
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_exercises_equipment FOREIGN KEY (equipment_id) REFERENCES equipment (id)
);

CREATE TABLE routines (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    name VARCHAR(120) NOT NULL,
    description VARCHAR(500),
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE workout_templates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    gym_id BIGINT,
    name VARCHAR(120) NOT NULL,
    description VARCHAR(500),
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_workout_templates_gym FOREIGN KEY (gym_id) REFERENCES gyms (id)
);

CREATE TABLE template_routines (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    workout_template_id BIGINT NOT NULL,
    routine_id BIGINT NOT NULL,
    notes VARCHAR(500),
    sort_order INTEGER NOT NULL,
    CONSTRAINT fk_template_routines_template FOREIGN KEY (workout_template_id) REFERENCES workout_templates (id),
    CONSTRAINT fk_template_routines_routine FOREIGN KEY (routine_id) REFERENCES routines (id)
);

CREATE TABLE template_exercises (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    workout_template_id BIGINT NOT NULL,
    template_routine_id BIGINT,
    exercise_id BIGINT NOT NULL,
    notes VARCHAR(500),
    sort_order INTEGER NOT NULL,
    planned_sets INTEGER,
    planned_reps INTEGER,
    planned_weight DECIMAL(10, 2),
    planned_duration_seconds INTEGER,
    CONSTRAINT fk_template_exercises_template FOREIGN KEY (workout_template_id) REFERENCES workout_templates (id),
    CONSTRAINT fk_template_exercises_template_routine FOREIGN KEY (template_routine_id) REFERENCES template_routines (id),
    CONSTRAINT fk_template_exercises_exercise FOREIGN KEY (exercise_id) REFERENCES exercises (id)
);

CREATE TABLE workout_sessions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    source_workout_template_id BIGINT NOT NULL,
    template_name_snapshot VARCHAR(120) NOT NULL,
    gym_id_snapshot BIGINT,
    gym_name_snapshot VARCHAR(120),
    status VARCHAR(40) NOT NULL,
    started_at TIMESTAMP NOT NULL,
    completed_at TIMESTAMP
);

CREATE TABLE session_routine_snapshots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    workout_session_id BIGINT NOT NULL,
    source_template_routine_id BIGINT NOT NULL,
    source_routine_id BIGINT NOT NULL,
    routine_name VARCHAR(120) NOT NULL,
    routine_description VARCHAR(500),
    notes VARCHAR(500),
    sort_order INTEGER NOT NULL,
    CONSTRAINT fk_session_routines_session FOREIGN KEY (workout_session_id) REFERENCES workout_sessions (id)
);

CREATE TABLE session_exercise_snapshots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    workout_session_id BIGINT NOT NULL,
    session_routine_snapshot_id BIGINT,
    source_template_exercise_id BIGINT NOT NULL,
    source_exercise_id BIGINT NOT NULL,
    source_equipment_id BIGINT,
    exercise_name VARCHAR(120) NOT NULL,
    muscle_group VARCHAR(120),
    instructions VARCHAR(1000),
    equipment_name VARCHAR(120),
    notes VARCHAR(500),
    sort_order INTEGER NOT NULL,
    planned_sets INTEGER,
    planned_reps INTEGER,
    planned_weight DECIMAL(10, 2),
    planned_duration_seconds INTEGER,
    CONSTRAINT fk_session_exercises_session FOREIGN KEY (workout_session_id) REFERENCES workout_sessions (id),
    CONSTRAINT fk_session_exercises_routine FOREIGN KEY (session_routine_snapshot_id) REFERENCES session_routine_snapshots (id)
);

CREATE TABLE workout_sets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    session_exercise_snapshot_id BIGINT NOT NULL,
    set_number INTEGER NOT NULL,
    reps INTEGER,
    weight DECIMAL(10, 2),
    duration_seconds INTEGER,
    notes VARCHAR(500),
    completed_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_workout_sets_session_exercise FOREIGN KEY (session_exercise_snapshot_id) REFERENCES session_exercise_snapshots (id)
);

CREATE INDEX idx_gyms_user_id ON gyms (user_id);
CREATE INDEX idx_equipment_user_id ON equipment (user_id);
CREATE INDEX idx_exercises_user_id ON exercises (user_id);
CREATE INDEX idx_routines_user_id ON routines (user_id);
CREATE INDEX idx_workout_templates_user_id ON workout_templates (user_id);
CREATE INDEX idx_template_routines_template ON template_routines (workout_template_id);
CREATE INDEX idx_template_exercises_template ON template_exercises (workout_template_id);
CREATE INDEX idx_workout_sessions_user_id ON workout_sessions (user_id);
CREATE INDEX idx_session_routines_session ON session_routine_snapshots (workout_session_id);
CREATE INDEX idx_session_exercises_session ON session_exercise_snapshots (workout_session_id);
CREATE INDEX idx_workout_sets_session_exercise ON workout_sets (session_exercise_snapshot_id);
