package com.thesis.workout.benchmark;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.ArrayList;
import java.util.List;
import java.util.Locale;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.boot.ApplicationArguments;
import org.springframework.boot.ApplicationRunner;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.context.ConfigurableApplicationContext;
import org.springframework.context.annotation.Profile;
import org.springframework.stereotype.Component;

/**
 * Orchestrates the OpenSearch-vs-PostgreSQL search benchmark and writes a Markdown report. Runs only
 * under the {@code benchmark} profile with search enabled. For each configured corpus size it loads
 * an identical synthetic corpus into both engines, then measures latency (p50/p95/p99) for a set of
 * representative query types after a warm-up, plus index build time and on-disk size.
 *
 * <p>Tunables (env or {@code -D}): {@code BENCH_SIZES} (default {@code 1000,10000}),
 * {@code BENCH_ITERS} (default 300), {@code BENCH_WARMUP} (default 50), {@code BENCH_TOPK}
 * (default 20). Report is written to {@code target/search-benchmark-results.md} and logged.</p>
 */
@Component
@Profile("benchmark")
@ConditionalOnProperty(name = "app.search.enabled", havingValue = "true")
public class SearchBenchmarkRunner implements ApplicationRunner {

    private static final Logger log = LoggerFactory.getLogger(SearchBenchmarkRunner.class);

    private final PostgresSearchBaseline postgres;
    private final OpenSearchBenchmark opensearch;
    private final ConfigurableApplicationContext context;

    public SearchBenchmarkRunner(PostgresSearchBaseline postgres, OpenSearchBenchmark opensearch,
            ConfigurableApplicationContext context) {
        this.postgres = postgres;
        this.opensearch = opensearch;
        this.context = context;
    }

    @Override
    public void run(ApplicationArguments args) {
        List<Integer> sizes = intList(env("BENCH_SIZES", "1000,10000"));
        int iters = Integer.parseInt(env("BENCH_ITERS", "300"));
        int warmup = Integer.parseInt(env("BENCH_WARMUP", "50"));
        int topK = Integer.parseInt(env("BENCH_TOPK", "20"));
        int loadBatch = Integer.parseInt(env("BENCH_LOAD_BATCH", "5000"));
        int minSamples = Integer.parseInt(env("BENCH_MIN_SAMPLES", "15"));
        long budgetNanos = Long.parseLong(env("BENCH_BUDGET_MS", "15000")) * 1_000_000L;

        log.info("Search benchmark starting: sizes={}, iters={}, warmup={}, topK={}, budgetMs={}",
                sizes, iters, warmup, topK, budgetNanos / 1_000_000L);
        postgres.ensureSchema();

        StringBuilder report = new StringBuilder();
        report.append("# Search benchmark results (OpenSearch vs PostgreSQL)\n\n");
        report.append("Generated by `SearchBenchmarkRunner` (profile `benchmark`). ");
        report.append(String.format(Locale.ROOT,
                "iters=%d (min %d), warmup=%d, topK=%d, per-query budget=%dms.%n%n",
                iters, minSamples, warmup, topK, budgetNanos / 1_000_000L));
        report.append("Latency is single-threaded, in **milliseconds**, measured in-JVM around each ")
                .append("query call (localhost, warm caches). Both engines index an identical synthetic ")
                .append("corpus. PG = weighted tsvector + GIN (full-text), pg_trgm + GIN (fuzzy), ")
                .append("GROUP BY (facets). OS = boosted multi_match, fuzziness=AUTO, terms agg.\n");

        List<Scenario> scenarios = scenarios(topK);

        for (int size : sizes) {
            log.info("=== corpus size {} ===", size);

            // Stream the (identical, deterministic) corpus into each engine; constant memory.
            postgres.prepareLoad();
            long pgStart = System.nanoTime();
            SyntheticCorpus.forEachBatch(size, loadBatch, postgres::insertBatch);
            postgres.finishLoad();
            long pgBuild = (System.nanoTime() - pgStart) / 1_000_000;

            opensearch.reset();
            long osStart = System.nanoTime();
            SyntheticCorpus.forEachBatch(size, loadBatch, opensearch::insertBatch);
            opensearch.finishLoad();
            long osBuild = (System.nanoTime() - osStart) / 1_000_000;

            long pgBytes = postgres.storageBytes();
            long osBytes = opensearch.storageBytes();
            log.info("size={} loaded: PG build={}ms OS build={}ms", size, pgBuild, osBuild);

            report.append(String.format(Locale.ROOT, "%n## Corpus size: %,d templates%n%n", size));
            report.append("| Metric | PostgreSQL | OpenSearch |\n|---|---:|---:|\n");
            report.append(String.format(Locale.ROOT, "| Index build time (ms) | %,d | %,d |%n", pgBuild, osBuild));
            report.append(String.format(Locale.ROOT, "| On-disk size (MB) | %.1f | %s |%n",
                    pgBytes / 1_048_576.0, osBytes < 0 ? "n/a" : String.format(Locale.ROOT, "%.1f", osBytes / 1_048_576.0)));
            report.append("\n| Query (p50 / p95 / p99 ms) | PostgreSQL | OpenSearch |\n|---|---:|---:|\n");

            for (Scenario sc : scenarios) {
                Measured pg = measure(warmup, iters, minSamples, budgetNanos, sc.pg());
                Measured os = measure(warmup, iters, minSamples, budgetNanos, sc.os());
                report.append(String.format(Locale.ROOT,
                        "| %s | %.3f / %.3f / %.3f%s | %.3f / %.3f / %.3f%s |%n",
                        sc.name(),
                        pg.stats().p50(), pg.stats().p95(), pg.stats().p99(), note(pg, iters),
                        os.stats().p50(), os.stats().p95(), os.stats().p99(), note(os, iters)));
                log.info("size={} {} -> PG p50={}ms (n={}) | OS p50={}ms (n={})",
                        size, sc.name(), pg.stats().p50(), pg.samples(), os.stats().p50(), os.samples());
            }
        }

        String md = report.toString();
        try {
            Path out = Path.of("target", "search-benchmark-results.md");
            Files.createDirectories(out.getParent());
            Files.writeString(out, md);
            log.info("Benchmark report written to {}", out.toAbsolutePath());
        } catch (IOException ex) {
            log.warn("Could not write benchmark report file: {}", ex.getMessage());
        }
        System.out.println("\n================ SEARCH BENCHMARK RESULTS ================\n" + md
                + "\n=========================================================\n");
        log.info("Search benchmark complete; shutting down.");
        // One-shot tool: exit cleanly once results are written so the JVM does not idle.
        System.exit(SpringApplication.exit(context, () -> 0));
    }

    private List<Scenario> scenarios(int topK) {
        List<Scenario> list = new ArrayList<>();
        list.add(new Scenario("full-text \"bench press\"",
                () -> postgres.fullText("bench press", topK),
                () -> opensearch.fullText("bench press", topK)));
        list.add(new Scenario("full-text \"squat\"",
                () -> postgres.fullText("squat", topK),
                () -> opensearch.fullText("squat", topK)));
        list.add(new Scenario("fuzzy \"benhc\" (typo)",
                () -> postgres.fuzzy("benhc", topK),
                () -> opensearch.fuzzy("benhc", topK)));
        list.add(new Scenario("filtered (press + INTERMEDIATE + PPL)",
                () -> postgres.filtered("press", "INTERMEDIATE", "PPL", topK),
                () -> opensearch.filtered("press", "INTERMEDIATE", "PPL", topK)));
        list.add(new Scenario("facet by difficulty (q=press)",
                () -> postgres.facetByDifficulty("press"),
                () -> opensearch.facetByDifficulty("press")));
        return list;
    }

    /**
     * Warms up then measures, but bounded by a wall-clock budget so an O(corpus) query (e.g. PG
     * fuzzy at 1M) cannot run for hours: it stops once at least {@code minSamples} are collected and
     * the budget is exceeded. The sample count is reported so few-sample cells are visible.
     */
    private static Measured measure(int warmup, int iters, int minSamples, long budgetNanos, Runnable op) {
        long warmStart = System.nanoTime();
        for (int i = 0; i < warmup; i++) {
            op.run();
            if (System.nanoTime() - warmStart > budgetNanos / 2) {
                break;
            }
        }
        long[] samples = new long[iters];
        int n = 0;
        long measureStart = System.nanoTime();
        for (int i = 0; i < iters; i++) {
            long t0 = System.nanoTime();
            op.run();
            samples[n++] = System.nanoTime() - t0;
            if (n >= minSamples && System.nanoTime() - measureStart > budgetNanos) {
                break;
            }
        }
        return new Measured(LatencyStats.of(java.util.Arrays.copyOf(samples, n)), n);
    }

    private static String note(Measured m, int iters) {
        return m.samples() < iters ? String.format(Locale.ROOT, " (n=%d)", m.samples()) : "";
    }

    /** A measured latency summary plus how many samples were actually collected. */
    private record Measured(LatencyStats stats, int samples) {
    }

    private static List<Integer> intList(String csv) {
        List<Integer> out = new ArrayList<>();
        for (String part : csv.split(",")) {
            String trimmed = part.trim();
            if (!trimmed.isEmpty()) {
                out.add(Integer.parseInt(trimmed));
            }
        }
        return out;
    }

    private static String env(String key, String fallback) {
        String value = System.getenv(key);
        if (value == null || value.isBlank()) {
            value = System.getProperty(key);
        }
        return value == null || value.isBlank() ? fallback : value;
    }

    /** A query type to compare; {@code pg}/{@code os} run the equivalent query on each engine. */
    private record Scenario(String name, Runnable pg, Runnable os) {
    }
}
